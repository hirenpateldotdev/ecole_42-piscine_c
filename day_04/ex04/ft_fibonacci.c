int		ft_fibonacci(int index)
{
	int n1;
	int n2;
	int t;

	n1 = 0;
	n2 = 1;
	if (index == 0)
		return (n1);
	if (index == 1)
		return (n2);
	while (index != 0)
	{
		t = n1 + n2;
		n1 = n2;
		n2 = t;
		index--;
	}
	return (n2);
}
