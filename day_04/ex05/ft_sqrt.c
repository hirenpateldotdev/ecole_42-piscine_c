int		ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 0;
	if (2147395600 < nb)
		return (0);
	while (sqrt < nb)
	{
		if ((sqrt * sqrt) == nb)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
